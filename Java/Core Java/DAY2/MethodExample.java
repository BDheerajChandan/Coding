
public class MethodExample 
{
	
	public static void findOddEven(int n)
	{
		
		
		if((n%2) == 0)
		{
			System.out.println("given number is even");
		}else
		{
			System.out.println("given number is odd");
		}
		
	}
	
	
	
	public static void main(String[] args) 
	{
		int num = 30;
		findOddEven(num);
		
	}
	
	
}
