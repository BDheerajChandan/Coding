class IncrementOperators {
    public void main() {
        int a = 20, b = 30;
        System.out.println("a =" +a);
        System.out.println("b =" +b);
        System.out.println("++a =" +(++a));
        System.out.println("++b =" +(++b));
        System.out.println("a++ =" +a++);
        System.out.println("b++ =" +b++);
        System.out.println("a =" +a);
        System.out.println("b =" +b);
    }
}


