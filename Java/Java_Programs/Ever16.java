//Use of Conditional Operators in Java
class Ever16
{
public void conditional()
{
int x = 10, y= 15, z=20;
System.out.println( "x = " +x);
System.out.println( "y = " +y);
System.out.println( "z = " +z);
System.out.println( "(x>y) ? x :y = " +((x>y) ? x: y));
System.out.println( "(z>y) ? z :y = " +((z>y) ? z: y));
System.out.println( "(z>x) ? z :x = " +((z>x) ? z: x));
}
}



