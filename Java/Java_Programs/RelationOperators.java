class RelationOperators {
public  void main() {
    int a = 25, b = 30, c = 40;;
    System.out.println("a =" +a);
    System.out.println("b =" +b);
    System.out.println("c =" +c);
    System.out.println("a>b=" +(a>b));
    System.out.println("a<b=" +(a<b));
    System.out.println("a>c=" +(a>c));
    System.out.println("a<c=" +(a<c));
    System.out.println("b<c=" +(b<c));
    System.out.println("b>c=" +(b>c));
    System.out.println("a==b=" +(a==b));
    System.out.println("b==c=" +(b==c));
    System.out.println("a!=c=" +(a!=c));
    System.out.println("a>=c=" +(a>=c));
   }
}


